# 1부터 6까지 숫자로 구성된 리스트 생성
Data = [i for i in range(1, 7)]  # [1, 2, 3, 4, 5, 6]

# 뒤집을 부분의 시작과 끝 인덱스 정의
start, end = 3, 5

# 리스트의 부분 구간을 잘라서 복사 (3~5번 인덱스: [4, 5, 6])
dummy = Data[3:6]

# 해당 부분을 역순으로 뒤집기
dummy.reverse()  # [6, 5, 4]

# 원래 리스트의 해당 구간에 역순 리스트를 다시 삽입
Data[start:end+1] = dummy

# 결과 출력
print(Data)  # [1, 2, 3, 6, 5, 4]