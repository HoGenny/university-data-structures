# 문자열 정의
str = 'this is sample string'

# 'i'의 첫 번째 등장 인덱스 (전체 문자열 기준)
c1 = str.find('i')  # → 첫 번째 'i'는 인덱스 2

# 인덱스 10부터 시작해서 'i'의 첫 등장 인덱스
c2 = str.find('i', 10)  # → 인덱스 13에 등장 ('string')

# 인덱스 0~9 사이에서의 'i' 첫 등장 인덱스
c3 = str.find('i', 0, 10)  # → 인덱스 2

# 결과 출력
print(c1)  # 전체에서 'i'의 첫 위치
print(c2)  # 인덱스 10부터의 'i' 첫 위치
print(c3)  # 인덱스 0~9 내의 'i' 첫 위치
