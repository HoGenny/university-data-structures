# 주어진 정수 리스트
data = [0, 4, 1, 3, 1, 2, 4, 1]

# 리스트에서 가장 큰 값을 찾음 (카운트 배열 크기 설정용)
iamMax = max(data)

# 각 숫자의 빈도수를 저장할 카운트 배열 생성 (모두 0으로 초기화)
count = [0] * (iamMax + 1)

# data 리스트의 각 요소를 순회하며 등장 횟수를 카운트
for i in range(len(data)):
    count[data[i]] += 1

# 결과 출력: 각 인덱스는 숫자를 의미하고, 값은 등장 횟수
print(count)  # 예: [1, 3, 1, 1, 2] → 1은 3번, 4는 2번 등장

### 이 코드는 카운팅 소트에서 사용하는 빈도수 배열 생성 방식의 핵심 로직으로
### 리스트에 어떤 숫자가 몇 번 나왔는지 빠르게 확인할 수 있습니다.